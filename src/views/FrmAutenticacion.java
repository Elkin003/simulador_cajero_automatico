    private void botonIngresar() {
        String nroCuenta = txtnrocuenta.getText();
        String pin = String.valueOf(txtpiningresado.getPassword());
        Autenticacion a = new Autenticacion();
        String[] resultado = a.autenticar(nroCuenta, pin);

        if (resultado[0].equals("BLOQUEADA")) {
            JOptionPane.showMessageDialog(this, "La cuenta estÃ¡ bloqueada.");
        } else if (resultado[0].equals("PIN INCORRECTO")) {
            intentos++;
            JOptionPane.showMessageDialog(this, "PIN incorrecto. Intento " + intentos + " de 3.");
            if (intentos >= 3) {
                a.bloquearCuenta(nroCuenta);
                JOptionPane.showMessageDialog(this, "Cuenta bloqueada por 3 intentos fallidos.");
            }
        } else if (resultado[0].equals("NO ENCONTRADA")) {
            JOptionPane.showMessageDialog(this, "Cuenta no encontrada.");
        } else {
            JOptionPane.showMessageDialog(this, "Bienvenido " + resultado[0]);
            intentos = 0;
            Session.setUser(resultado);
            new FrmInterfazPrincipal().setVisible(true);
            dispose();
        }
    }

    private void btnRegistrar() {
        if (txtnrocuenta.getText().equals("ADMINISTRADOR") && String.valueOf(txtpiningresado.getPassword()).equals("999999")) {
            new FrmUsuario(null, true).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "ACCESO NO CONCEDIDO", "ACCESO DENEGADO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void botonesNumeros(String numero) {
        if (focusNroCuenta) {
            String textoActual = txtnrocuenta.getText();
            String nuevoTexto = textoActual + numero;
            txtnrocuenta.setText(nuevoTexto);
        } else if (focusPinIngresado) {
            String textoActual = txtpiningresado.getText();
            String nuevoTexto = textoActual + numero;
            txtpiningresado.setText(nuevoTexto);
        }
    }

    private void botonBorrar() {
        if (focusNroCuenta) {
            String textoActual = txtnrocuenta.getText();
            if (!textoActual.isEmpty()) {
                txtnrocuenta.setText(textoActual.substring(0, textoActual.length() - 1));
            }
        } else if (focusPinIngresado) {
            String textoActual = txtpiningresado.getText();
            if (!textoActual.isEmpty()) {
                txtpiningresado.setText(textoActual.substring(0, textoActual.length() - 1));
            }
        }
    }

